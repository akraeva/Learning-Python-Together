def m_5_3_6():
    """
    Цветовая головоломка
    -------------------------------------
    Давайте с вами решим такую головоломку. Нам даны два пикселя.
    Каждый пиксель представляется тремя значением в формате (R, G, B),
    где каждая составляющая представлена в 16-ричной системе.

    Итого у нас 6 входящих строк RGB первого пикселя и RGB второго пикселя.

    Необходимо найти средний цвет между двумя пикселями,
    с усреднением значений R, G, B для каждого пикселя.

    Находим средний пиксель через целочисленное деление.

    Примечание: При переводе в шестнадцатеричную систему не забываем,
    что нам нужно избавиться от первых двух символов, указывающих на кодировку.
    """
    pxl1, pxl2 = [[int(input(), 16) for _ in range(3)] for _ in range(2)]
    pxl = map(lambda x: hex(sum(x) // 2)[2:], zip(pxl1, pxl2))
    print(*pxl)
