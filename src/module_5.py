def m_5_3_3():
    """
    Идеальная фигура
    -------------------------------------
    Анжела следит за своей формой, как мы уже поняли по прошлым задачам.
    Теперь она хочет узнать, насколько ее фигура отклоняется от идеальных
    пропорций 90-60-90. Для этого необходимо определить разницу между
    параметрами ее фигуры и идеальными значениями.

    На вход подаются три значения, целые числа:
    ширина плеч, обхват талии и обхват бедер.

    Вывести нужно три значения:
        - разницу между шириной плеч Анжелы и идеальными 90 см,
        - разницу между обхватом талии Анжелы и идеальными 60 см,
        - а также разницу между обхватом бедер Анжелы и идеальными 90 см.

    Ответ должен иметь следующий вид:
        - В плечах <больше/меньше> на x см.
        - Обхват талии <больше/меньше> на y см.
        - Обхват бедер <больше/меньше> на z см.

    Следовательно нам нужно вывести больше или меньше в зависимости от разницы
    амеренной и идеальной фигурой.
    А само число итого вывести в виде числа, взятого по модулю.
    """
    og, ot, ob = (int(input()) for _ in range(3))
    imperfect = lambda num, it: "больше" if num > it else "меньше"
    res = (
        f"В плечах {imperfect(og, 90)} на {abs(og - 90)} см.",
        f"Обхват талии {imperfect(ot, 60)} на {abs(ot - 60)} см.",
        f"Обхват бедер {imperfect(ob, 90)} на {abs(ob - 90)} см.",
    )
    print(*res, sep="\n")


