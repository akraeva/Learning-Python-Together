def m_4_2_2():
    """
    Количество чётных чисел
    -------------------------------------
    Дана последовательность целых чисел. Перестать считывать последовательность
    нужно когда встретиться число равное либо меньше 0.

    Вам предстоит найти количество чисел в последовательности,
    которые являются чётными.
    """
