def m_4_5_1():
    """
    Ох уж этот BMW
    -------------------------------------
    *Вася был в седле своего долгожданного BMW 3, как из рекламы "most wanted"
    он наслаждался каждой поездкой. Однако в один прекрасный день его мечта
    рухнула, словно студент перед экзаменом, пытающийся выучить формулы за
    пять минут до начала. Оказалось, что Васе несказанно не повезло.
    У нас теперь есть одна весьма конкретная ироничная ситуация.
    У Васи есть некая сумма денег "x" для ремонта своего авто, но
    несчастье - сервис постоянно звонит и присылает счета за ремонт.*

    Нам нужна программа, которая будет учитывать, сколько денег Вася готов
    потратить, а затем последовательно вычитать из этой суммы счета за ремонт
    до тех пор, пока не дойдем до нуля в этой последовательности чисел
    (будем с вами считать, условно, что это звонок, что Вася может
    забрать своё авто).

    Если при вычете очередного чека у нас возникает отрицательное значение,
    то программа должна вывести сообщение:

        ``Вася, скорее продавай, тебе же нужно что-то кушать!``

    А если последовательность счетов доводит нас до нуля, не перейдя
    в отрицательное значение, то мы выведем:

        ``Ура, скоро станем королями дороги.
        С тебя <сумма> рублей, у тебя осталось <остаток> рублей.``
    """
    money = int(input())
    cost = 0
    while (price := int(input())) != 0:
        cost += price
        if money < cost:
            print("Вася, скорее продавай, тебе же нужно что-то кушать!")
            break
    else:
        print(
            f"Ура, скоро станем королями дороги. С тебя {cost} рублей, "
            f"у тебя осталось {money - cost} рублей."
        )


def m_4_5_2():
    """
    Факториал числа
    -------------------------------------
    Вам необходимо вычислить факториал числа.

    *Немного теории: Факториал числа - это произведение всех целых чисел
    от 1 до данного числа, включительно. Символически обозначается как n!*
    """
    n = int(input())
    if n > 0:
        f = n
        for i in range(1, n):
            f *= i
    elif n == 0:
        f = 1
    else:
        f = "Факториал определен только для неотрицательных целых чисел"
    print(f)


