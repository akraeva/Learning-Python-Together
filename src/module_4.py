def m_4_4_11():
    """
    Счастливый билет?
    -------------------------------------
    На вход подаётся номер билета - целочисленное значение x
    (100000 <= x <= 999999), необходимо проверить является ли билет счастливым.

    *Счастливым билетом считается если сумма первых трёх цифр числа и сумма
    последних трёх цифр числа равны.*

    Если билет счастливый, то вывести "Счастливый билет",
    иначе вывести "Несчастливый билет"
    """

    def num_sum(n):
        res = n // 100 + n // 10 % 10 + n % 10
        return res

    number = int(input())
    num_left, num_right = number // 1000, number % 1000
    if num_sum(num_left) == num_sum(num_right):
        print("Счастливый билет")
    else:
        print("Несчастливый билет")


def m_4_4_12():
    """
    Счастливые билеты
    -------------------------------------
    На вход подаются два трёхзначных целочисленных значения x и y (x < y).

    Напишите программу, которая будет находить и выводить все счастливые
    билеты в заданном диапазоне по y включительно.

    *Счастливый билет - это такой номер билета, у которого сумма первых
    трех цифр равна сумме последних трех цифр.*
    """

    def num_sum(n):
        res = n // 100 + n // 10 % 10 + n % 10
        return res

    def happy(n):
        num_left, num_right = n // 1000, n % 1000
        return num_sum(num_left) == num_sum(num_right)

    num_start, num_end = int(input()), int(input())
    res = (num for num in range(num_start, num_end + 1) if happy(num))
    print(*res, sep="\n")


