def m_4_4_15():
    """Больше предыдущего?
    Дана последовательность целочисленных чисел (число может быть
    положительным или отрицательным, каждое число с новой строки),
    завершающаяся нулем. Ноль не является частью последовательности!
    Нужно определить количество элементов последовательности,
    которые превышают значения своих предыдущих чисел.
    Гарантировано, что последовательность чисел больше двух значений.
    """
    count = 0
    num = int(input())
    while (next_num := int(input())) != 0:
        if next_num > num:
            count += 1
        num = next_num
    print(count)
