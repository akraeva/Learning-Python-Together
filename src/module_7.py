def m_7_8_1():
    """
    Формируем матрицу из входных данных
    --------------------------------------
    На вход поступает целое число n, за ним следует n строк, каждая строка
    представляет из себя числа, разделённые пробелом.

    Считайте матрицу, переведя из строк числа в целочисленные значения
    и выведите её полностью на печать.
    """
    matrix = [list(map(int, input().split())) for _ in range(int(input()))]
    print(matrix)


def m_7_8_2():
    """
    Поиск элемента в матрице
    ---------------------------
    На вход поступает целое число n, за ним следует n строк, каждая строка
    представляет из себя числа, разделённые пробелом, следом поступает
    целочисленное значение, которое необходимо найти в матрице.

        - если данное число есть в матрице, то вывести
            индекс строки и через пробел индекс столбца,

        - в противном случае, если элемент в матрице не обнаружен вывести сообщение:
            ``Элемент <искомое значение> не найден!``
    """
    matrix = [list(map(int, input().split())) for _ in range(int(input()))]
    num = int(input())
    try:
        res = next((i, line.index(num)) for i, line in enumerate(matrix) if num in line)
    except:
        res = (f"Элемент {num} не найден!",)
    print(*res)


def m_7_8_3():
    """
    Подсчёт суммы столбцов матрицы
    -------------------------------
    На вход поступает целое число n, за ним следует n строк,
    каждая строка представляет из себя числа, разделённые пробелом.
    Необходимо сформировать матрицу с целочисленными значениями.

    Программа должна вывести на печать список
    состоящий из суммы каждого столбца матрицы.
    """
    matrix = [list(map(int, input().split())) for _ in range(int(input()))]
    col_sum = list(map(sum, zip(*matrix)))
    print(col_sum)


def m_7_8_4():
    """
    Транспонирование матрицы
    -------------------------------
    На вход поступает целое число n, за ним следует n строк,
    каждая строка представляет из себя числа, разделённые пробелом.
    Необходимо сформировать матрицу с целочисленными значениями.

    Необходимо написать программу, которая преобразует столбцы матрицы
    в строки и строки в столбцы. Вывести итоговую матрицу на печать.
    Каждую строку отдельно, элементы строк итоговой матрицы должны
    быть разделены между собой пробелами.
    """
    matrix = [list(map(int, input().split())) for _ in range(int(input()))]

    t_matrix = list(zip(*matrix))
    for line in t_matrix:
        print(*line)
