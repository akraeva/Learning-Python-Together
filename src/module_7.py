def m_7_7_6():
    """
    Создаём лист с помощью спискового включения с несколькими правилами
    ---------------------------------------------------------------------
    Необходимо написать программу, которая считывает список целых чисел,
    разделенных пробелами, и применяет следующее правило к каждому элементу:

        - если меньше, то вообще не рассматриваем данное число для добавления
        - если число больше 100, программа проверяет, является ли число четным:
            - Если число четное, оно остается без изменений,
            - В противном случае к числу прибавляется 1.

    Затем программа формирует новый список, содержащий преобразованные
    числа, и выводит его.
    """
    nums = [
        num if num % 2 == 0 else num + 1
        for num in map(int, input().split())
        if num > 100
    ]
    print(nums)


